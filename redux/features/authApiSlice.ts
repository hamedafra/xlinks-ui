import { apiSlice } from '../services/apiSlice';

interface User {
	first_name?: string;
	last_name?: string;
	email: string;
}


interface UserListItem {
    id: number;
    email: string;
    first_name: string;
    last_name: string;
}

interface SocialAuthArgs {
	provider: string;
	state: string;
	code: string;
}

interface CreateUserResponse {
	success: boolean;
	user: User;
}

interface UserProfile {
    id: number;
    email: string;
    first_name?: string;
    last_name?: string;
    bio?: string;
    traffic_limit?: number;
    traffic_used?: number;
    ip?: string ;
    mobile?: string ;
    avatar?: string;
    is_active?: boolean;
    is_staff?: boolean;
    is_superuser?: boolean;
    date_joined?: Date;
}

const authApiSlice = apiSlice.injectEndpoints({
	endpoints: builder => ({
		retrieveUser: builder.query<User, void>({
			query: () => '/account/',
		}),
		socialAuthenticate: builder.mutation<
			CreateUserResponse,
			SocialAuthArgs
		>({
			query: ({ provider, state, code }) => ({
				url: `/o/${provider}/?state=${encodeURIComponent(
					state
				)}&code=${encodeURIComponent(code)}`,
				method: 'POST',
				headers: {
					Accept: 'application/json',
					'Content-Type': 'application/x-www-form-urlencoded',
				},
			}),
		}),
		login: builder.mutation({
			query: ({ email, password }) => ({
				url: '/jwt/create/',
				method: 'POST',
				body: { email, password },
			}),
		}),
		register: builder.mutation({
			query: ({
				first_name,
				last_name,
				email,
				password,
				re_password,
			}) => ({
				url: '/users/',
				method: 'POST',
				body: { first_name, last_name, email, password, re_password },
			}),
		}),
		verify: builder.mutation({
			query: () => ({
				url: '/jwt/verify/',
				method: 'POST',
			}),
		}),
		logout: builder.mutation({
			query: () => ({
				url: '/logout/',
				method: 'POST',
			}),
		}),
		activation: builder.mutation({
			query: ({ uid, token }) => ({
				url: '/users/activation/',
				method: 'POST',
				body: { uid, token },
			}),
		}),
		resetPassword: builder.mutation({
			query: email => ({
				url: '/users/reset_password/',
				method: 'POST',
				body: { email },
			}),
		}),
		resetPasswordConfirm: builder.mutation({
			query: ({ uid, token, new_password, re_new_password }) => ({
				url: '/users/reset_password_confirm/',
				method: 'POST',
				body: { uid, token, new_password, re_new_password },
			}),
		}),
		downloadInitiate: builder.mutation({
            query: ({ url }) => ({
                url: 'https://mylinks.ir/api/leecher/download/initiate/',
                method: 'POST',
				body: { url },
            }),
        }),
		adminListUsers: builder.query<UserListItem[], void>({
			query: () => '/admin/listusers/', 
		}),
		adminDeleteUser: builder.mutation({
            query: (userId) => ({
                url: `/admin/account/${userId}/delete/`,
                method: 'DELETE',
            }),
        }),
		adminFetchUserById: builder.query<UserProfile, number>({
			query: (userId) => `/admin/user/${userId}/`,
		  }),
		adminUpdateUser: builder.mutation<UserProfile, UserProfile>({
			query: (userUpdates) => ({
				url: `/admin/user/${userUpdates.id}/`,
				method: 'PUT',
				body: userUpdates,
			}),
		}),
	}),
});

export const {
	useRetrieveUserQuery,
	useSocialAuthenticateMutation,
	useLoginMutation,
	useRegisterMutation,
	useVerifyMutation,
	useLogoutMutation,
	useActivationMutation,
	useResetPasswordMutation,
	useResetPasswordConfirmMutation,    
	useDownloadInitiateMutation,
	useAdminListUsersQuery,
	useAdminDeleteUserMutation,
	useAdminFetchUserByIdQuery,
	useAdminUpdateUserMutation,
} = authApiSlice;
